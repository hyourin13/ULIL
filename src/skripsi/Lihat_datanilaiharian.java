     public void Tampil_Tabel() {

        tabel2.setNumRows(0);
        try {
            String sql = "Select * from penilaian";
            Class.forName("com.mysql.jdbc.Driver");
            Connection P21Q = DriverManager.getConnection("jdbc:mysql:///skripsi", "root", "");
            PreparedStatement UR = P21Q.prepareStatement(sql);
            ResultSet Tampil = UR.executeQuery();
            while (Tampil.next()) {
                tabel2.addRow(new Object[]{Tampil.getString(1), Tampil.getString(2), Tampil.getString(3), Tampil.getString(4),
                    Tampil.getString(5),Tampil.getString(6), Tampil.getString(7), Tampil.getString(8), Tampil.getString(9),
                 Tampil.getString(10), Tampil.getString(11), Tampil.getString(12)});
                table_pdb.setModel(tabel2);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

